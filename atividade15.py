#17. Leia quatro números e calcule a média aritmética e imprima o resultado.

NUMERO1 = float( input("Digite o primeiro numero : ") )
NUMERO2 = float( input("Digite o segundo  numero : ") )
NUMERO3 = float( input("Digite o terceiro  numero : ") )
NUMERO4 = float( input("Digite o quarto e ultimo numero numero : ") )

SOMA = NUMERO1 + NUMERO2 + NUMERO3 + NUMERO4 

MEDIA = SOMA / 4 

print ( f"A sua  média aritimética é  : {MEDIA}")